import numpy as np

# Define custom numpy arrays A and B
A = np.array([1, 2, 3, 4, 5])
B = np.array([4, 5, 6, 7, 8])

# Stack A and B vertically
vertical_stack = np.vstack((A, B))

# Stack A and B horizontally
horizontal_stack = np.hstack((A, B))

print("Vertical Stack:\n", vertical_stack)
print("Horizontal Stack:\n", horizontal_stack)
common_elements = np.intersect1d(A, B)
print("Common Elements:", common_elements)
# Extract numbers between 5 and 10
filtered_numbers = A[(A >= 5) & (A <= 10)]
print("Numbers between 5 and 10:", filtered_numbers)
url = 'https://archive.ics.uci.edu/ml/machine-learning-databases/iris/iris.data'
iris_2d = np.genfromtxt(url, delimiter=',', dtype='float', usecols=[0, 1, 2, 3])

# Filter rows where petallength (3rd column) > 1.5 and sepallength (1st column) < 5.0
filtered_iris = iris_2d[(iris_2d[:, 2] > 1.5) & (iris_2d[:, 0] < 5.0)]
print("Filtered Iris Data:\n", filtered_iris)
import pandas as pd

df = pd.read_csv('https://raw.githubusercontent.com/selva86/datasets/master/Cars93_miss.csv')

# Select every 20th row starting from the first
filtered_df = df[['Manufacturer', 'Model', 'Type']].iloc[0::20]
print("Filtered DataFrame:\n", filtered_df)
# Replace missing values in Min.Price and Max.Price with their means
df['Min.Price'].fillna(df['Min.Price'].mean(), inplace=True)
df['Max.Price'].fillna(df['Max.Price'].mean(), inplace=True)

print("DataFrame after replacing missing values:\n", df[['Min.Price', 'Max.Price']].head())
